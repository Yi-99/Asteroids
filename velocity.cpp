#include "velocity.h"


/*SET DX
*	Input:
*		Dx: Float value that determines the X acceleration of the lander in the game
*************************************************************************************/
void Velocity::setDx(float x)
{
	Dx = x;
}

/*SET DY
*	Input:
*		Dy: Float value that determines the Y acceleration of the lander in the game
*************************************************************************************/
void Velocity::setDy(float y)
{
	Dy = y;
}
